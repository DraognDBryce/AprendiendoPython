# OPERADORES ARITMETICOS
num1 = 10
num2 = 5
resultado = num1 + num2
print(resultado)

resultado = num1 - num2
print(resultado)

resultado = num1 * num2
print(resultado)

resultado = num1 / num2
print(resultado)
#Division pero resultado entero hacia abajo
resultado = num1 // num2
print(resultado)

# Resto de la division
resultado = num1 % num2
print(resultado)

# Exponenciacion, es decir en este caso 10 a la 5 potencia
resultado = num1 ** num2
print(resultado)
# OPERADORES INCREMENTO, DECREMENTO

year = 2021

# Pre incremento
year = 1 + year
print(year)

# Pre decremento
year = 1 - year
print(year)

year = 2021
#incremento
year -=1
print(year)

#decremento
year +=1
print(year)