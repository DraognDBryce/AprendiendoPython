"""
- Pedir dos numeros al usuario y hacer todas las operaciones basicas de una calculadora y mostrarlo
"""

numero1 = int(input("Indique su primer numero: "))
numero2 = int(input("Indique su segundo numero: "))

print("Suma: " + str(numero1 + numero2))
print("Resta: "+ str(numero1 - numero2))
print("Multiplicacion" + str(numero1 * numero2))
print("Division" + str(numero1 / numero2))